package com.calbitica.app.Models.Habitica;

public class Hair {
    private String color;
    private Float base, bangs, beard, mustache, flower;

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public Float getBase() {
        return base;
    }

    public void setBase(Float base) {
        this.base = base;
    }

    public Float getBangs() {
        return bangs;
    }

    public void setBangs(Float bangs) {
        this.bangs = bangs;
    }

    public Float getBeard() {
        return beard;
    }

    public void setBeard(Float beard) {
        this.beard = beard;
    }

    public Float getMustache() {
        return mustache;
    }

    public void setMustache(Float mustache) {
        this.mustache = mustache;
    }

    public Float getFlower() {
        return flower;
    }

    public void setFlower(Float flower) {
        this.flower = flower;
    }
}
